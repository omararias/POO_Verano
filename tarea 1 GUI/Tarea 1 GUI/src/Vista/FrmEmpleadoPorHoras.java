    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        // TODO add your handling code here:
        String nombre = this.txtNombre.getText();
        String apellidoP = this.txtApellidoP.getText();
        String apellidoM = this.txtApellidoM.getText();
        String seguroS = this.txtSeguroS.getText();
        double salario = Double.parseDouble(this.txtSueldo.getText());
        double horas = Double.parseDouble(this.txtHoras.getText());

        EmpleadoPorHoras unEmpleado = new EmpleadoPorHoras(nombre, apellidoP, apellidoM, seguroS, salario, horas);
        Inicio.porPagarLista.add(unEmpleado);
        JOptionPane.showMessageDialog(rootPane, unEmpleado);
        limpiarCampos();
    }//GEN-LAST:event_btnGuardarMouseClicked


