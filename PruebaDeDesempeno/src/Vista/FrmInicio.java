    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            int indice=Integer.parseInt(this.txtIndice.getText());
            int elemento=Integer.parseInt(this.txtNuevoElemento.getText());
            Main.actualizarArreglo(indice, elemento);
            JOptionPane.showMessageDialog(rootPane, "Â¡Elemento modificado con exito!");
            
        }catch (ArrayIndexOutOfBoundsException eIndex){
            JOptionPane.showMessageDialog(rootPane, "Index fuera de limites del arreglo, 0-19");
            StackTraceElement element = eIndex.getStackTrace()[0];
            String className = element.getClassName();
            String fileName = element.getFileName();
            String methodName = element.getMethodName();
            int lineNumber = element.getLineNumber();

            // Crea un objeto de la clase Error con los detalles de la excepciÃ³n
            Errores error = new Errores(className, fileName, methodName, lineNumber);
            Main.erroresLista.add(error);
            
        }catch (NumberFormatException eFormat){
            JOptionPane.showMessageDialog(rootPane, "Ingrese solo numeros enteros como indices y valores");
            StackTraceElement element = eFormat.getStackTrace()[0];
            String className = element.getClassName();
            String fileName = element.getFileName();
            String methodName = element.getMethodName();
            int lineNumber = element.getLineNumber();

            // Crea un objeto de la clase Error con los detalles de la excepciÃ³n
            Errores error = new Errores(className, fileName, methodName, lineNumber);
            Main.erroresLista.add(error);

            
        }finally{
            this.txtIndice.setText("");
            this.txtNuevoElemento.setText("");
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

